# Day 4 of Python learning
# Exploring string methods

name = "   Gaurab Tiwary   "

# Common string methods
print(name.upper())          
print(name.lower())          
print(name.strip())          
print(name.replace("Tiwary", "T"))  
print(name.find("Tiwary"))   
print(name.count("a"))       

# More examples
quote = "coding is fun and exciting"
print(quote.capitalize())   
print(quote.title())         
print(quote.startswith("coding"))  
print(quote.endswith("fun"))       
