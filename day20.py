# 2️ Even or Odd number
num = 11
result = "Even" if num % 2 == 0 else "Odd"
print(result)  

# 3️ Login check
is_logged_in = True
message = "Welcome back!" if is_logged_in else "Please log in"
print(message)  

# 4️ Grading system
score = 75
grade = "Pass" if score >= 40 else "Fail"
